public class Direction {

    private final Integer directionX;
    private final Integer directionY;

    public Direction(int x, int y) {
        this.directionX = x;
        this.directionY = y;
    }

    public Integer getDirectionX() {
        return directionX;
    }

    public Integer getDirectionY() {
        return directionY;
    }
}
